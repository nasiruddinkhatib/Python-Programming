#Python has a built-in module that you can use for mathematical tasks.
#The math module has a set of methods and constants.
import math
x=math.sqrt(25)  # math.square root
print(x)

y=math.ceil(4.2)  # math.ceil
print(y)

f=math.floor(5.7)   # math.floor
print(f)

p=math.pow(2,2)  # Power Function
print(p)

print(math.pi)    # pie

fact=math.factorial(6)  # Factorial of number
print(fact)


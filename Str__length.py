# Define a function that computes the length of a given list or string.
a = "nasir"  #string
count = 0    #The variable count is initialized to 0. This variable will be used to keep track of the number of characters.
for char in a:  #for loop iterates over each character in a string a
    count += 1  #For each character, the value of count is incremented by 1 (count += 1)
    print(count)
